left_var: int = int(input("Left-hand side: "))
right_var: int = int(input("Right-hand side: "))
print(str(left_var) + " ** " + str(right_var) + " is " + str(left_var ** right_var))
print(str(left_var) + " / " + str(right_var) + " is " + str(left_var / right_var))
print(str(left_var) + " // " + str(right_var) + " is " + str(left_var // right_var))
print(str(left_var) + " % " + str(right_var) + " is " + str(left_var % right_var))